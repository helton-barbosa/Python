# Tipo de dado bool (boolean)
# Ao questionar algo em um programa, só existem duas respostas possíveis:
# sim (True) ou não (False).
# Usa-se os operadores lógicos para "questionar" os valores.

booleano_1 = 10 == 10
booleano_2 = 10 == 11
tipo = type(booleano_1)

print(booleano_1) # Saída: True
print(booleano_2) # Saída: False
print(tipo) # Saída: <class 'bool'> 

