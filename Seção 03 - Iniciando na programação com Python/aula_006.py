# conversão de tipos, coerção, type convertion, typecasting, coercion
# é o ato de converter um tipo em outro
# tipos imutáveis e primitivos:
# str, int, float, bool

# soma_incorreta = '1' + 1
# print(soma_incorreta)

soma_correta = int('1') + 1
print(soma_correta)

soma_float = float('10') + 10
print(soma_float)

caracteres = str(1) + '2'
print(caracteres)

booleano = bool('1')
print(booleano)

